class Player:
    def __init__(self, player_number, name, is_imposter):
        self.player_number = player_number
        self.name = name
        self.is_imposter = is_imposter
        self.taxi_count = 40
        self.bus_count = 16
        self.train_count = 5
        self.tram_count = 4

def get_player_count():
    return int(raw_input("how many players are there?\n"))

def get_players(player_count):
    imposter_decided = False
    players = []
    for player_number in range(1, player_count +1):
        prompt = "what is player {player_number}'s name\n".format(player_number=player_number)
        name = raw_input(prompt)
        is_imposter = False
        if not imposter_decided:
            is_imposter = raw_input("are you the imposter?\ny/n\n") == "y"
            imposter_decided += is_imposter
        players.append(Player(player_number, name, is_imposter))
    return players

